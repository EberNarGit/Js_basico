// Operadores
    // Operacion
        // Suma
        console.log(`3+2: ${3+2} `);

        // Resta
        console.log(`50-20: ${50-20}`);

        // Multiplicacion
        console.log(`50*20: ${50*20}`);

        // Division
        console.log(`50/20: ${50/20}`);

        // Concatenacion
        console.log(`"eber" + "eliud" : ${"eber" + "eliud"}`);

        // Negacion
        console.log(`!false: ${!false}`);
    
    // Asignacion
        // =
        console.log(`var a = 1: ${1}`);

        // == comparacion
        console.log(`3=="3": ${3=="3"}`);

        // === si o si
        console.log(`3==="3": ${3==="3"}`);

        // < menor
        console.log(`3<3: ${3<3}`);

        // > mayor
        console.log(`3>3: ${3>3}`);

        // <= menor igual
        console.log(`3<=3: ${3<=3}`);

        // => mayor igual
        console.log(`3>=3: ${3>=3}`);

        // && doble condicion
        var a = true
        var b = true
        console.log(`a&&b: ${a&&b}`);

        // || alguna de las condiciones
        console.log(`!a||b: ${!a||b}`);

        // ++ incrementa valor
        var edad = 21
        edad++
        console.log(`edad++: ${edad++}`);
        console.log(`edad+=2: ${edad+=2}`);
